func fibonacci(n:Int) -> Int {
    var result = 0
    print("Calculating fibonacci(\(n))...")
    if n <= 2 {
        print("Terminal case, returning 1.")
        return 1
    }else{
        result = fibonacci(n:n-1) + fibonacci(n:n-2)
        print("Non-terminal case, returning \(result).")
        return result
    }
}

var fibResult = fibonacci(n:n)
print("The final result is: \(fibResult).")
